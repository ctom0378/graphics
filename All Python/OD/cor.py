import turtle as t

tim=t.Turtle()

tim.speed(10000)



tim.pu()
tim.setpos(-340,280)
tim.pencolor('lightgreen')
tim.pd()
tim.fd(680)
for _ in range(28):
    tim.rt(90)
    tim.fd(10)
    tim.rt(90)
    tim.fd(680)
    tim.lt(90)
    tim.fd(10)
    tim.lt(90)
    tim.fd(680)
tim.lt(90)
tim.fd(560)

for _ in range(34):
    tim.lt(90)
    tim.fd(10)
    tim.lt(90)
    tim.fd(560)
    tim.rt(90)
    tim.fd(10)
    tim.rt(90)
    tim.fd(560)

tim.pu()
tim.home()
tim.pencolor('black')
tim.pd()
tim.fd(340)
tim.bk(10)
tim.pu()
tim.rt(90)
tim.fd(10)
tim.pd()
tim.write("x")
tim.pu()
tim.home()
tim.pd()
tim.bk(340)
tim.home()
tim.lt(90)
tim.fd(280)
tim.bk(10)
tim.pu()
tim.rt(90)
tim.fd(10)
tim.pd()
tim.write("y")
tim.pu()
tim.home()
tim.pd()
tim.lt(90)
tim.bk(280)

