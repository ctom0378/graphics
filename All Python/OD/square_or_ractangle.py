import turtle
t=turtle.Turtle()
t.penup()
t.goto(400,0)
t.pendown()
t.pensize(1)
t.pencolor("Black")
t.backward(800)
t.goto(0,0)
t.right(90)
t.forward(300)
t.backward(600)
t.forward(150)
t.right(90)
t.forward(200)
t.left(90)
t.forward(300)
t.left(90)
t.forward(400)
t.left(90)
t.forward(300)
t.left(90)
t.forward(200)
turtle.done()
